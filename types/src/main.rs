fn main() {
    
    // i8, i16, i32, i64, i128 -> Sin signo +/-
    // u8, u16, u32, u64, u128 -> Sin signo +

    let number_i: i8 = -10;
    let number_u: u8 = 10;

    let caracter = 'a';
    let real: f32 = 12.5;

    let resultado: bool = false;

    println!("i={} u={} caracter={} real={} bool={}", number_i, number_u, caracter, real, resultado);
}
